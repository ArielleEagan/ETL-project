	Title VARCHAR PRIMARY KEY,
	Year Date,
	IMDb VARCHAR,
	Rotten Tomatoes VARCHAR,
	Netflix INT,
	Hulu INT,
	Prime Video INT,
	Disney+ INT,
	Genres VARCHAR);